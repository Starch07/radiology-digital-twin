import csv
import json
import os
from collections import Counter
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt
import matplotlib.patches as mpatches
import numpy as np

# ── Load data ──────────────────────────────────────────────────────────────
rows = list(csv.DictReader(open('radiology_pipeline/results/summary.csv', encoding='utf-8', errors='ignore')))
print(f"Loaded {len(rows)} patient records")

# ── Extract fields ─────────────────────────────────────────────────────────
priorities = [r['triage_priority'].strip() for r in rows if r['triage_priority'].strip()]
priority_counts = Counter(priorities)

red_flags = [r['red_flags'].strip() for r in rows]
has_red_flag = sum(1 for f in red_flags if f and f.lower() not in ['none identified', 'none', ''])
no_red_flag = len(rows) - has_red_flag

specialties = []
for r in rows:
    if r['specialist_referrals'].strip():
        for p in r['specialist_referrals'].split('&'):
            s = p.strip().split('(')[0].strip()
            if s:
                specialties.append(s)
spec_counts = Counter(specialties)

parse_ok = sum(1 for r in rows if r.get('parse_status','').strip().lower() == 'ok')
parse_fail = len(rows) - parse_ok

priority_labels = {
    '1': 'P1-Critical', '2': 'P2-Emergency',
    '3': 'P3-Urgent', '4': 'P4-Semi-urgent', '5': 'P5-Non-urgent'
}
priority_colors = ['#E24B4A','#EF9F27','#378ADD','#1D9E75','#888780']

os.makedirs('radiology_pipeline/results/evaluation_plots', exist_ok=True)

plt.style.use('seaborn-v0_8-whitegrid')

# ── Plot 1: Triage Priority Distribution (Bar) ─────────────────────────────
fig, ax = plt.subplots(figsize=(10, 6))
keys = sorted(priority_counts.keys())
labels = [priority_labels.get(k, k) for k in keys]
values = [priority_counts[k] for k in keys]
colors = [priority_colors[int(k)-1] for k in keys]
bars = ax.bar(labels, values, color=colors, edgecolor='white', linewidth=1.5, width=0.6)
for bar, val in zip(bars, values):
    ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.3, str(val),
            ha='center', va='bottom', fontweight='bold', fontsize=12)
ax.set_title('Figure 4.1: Triage Priority Distribution of 100 Brain MRI Patients', fontsize=14, fontweight='bold', pad=15)
ax.set_xlabel('Triage Priority Level', fontsize=12)
ax.set_ylabel('Number of Patients', fontsize=12)
ax.set_ylim(0, max(values) + 5)
plt.tight_layout()
plt.savefig('radiology_pipeline/results/evaluation_plots/fig4_1_triage_distribution.png', dpi=150, bbox_inches='tight')
plt.close()
print("Plot 1 saved")

# ── Plot 2: Triage Priority Pie Chart ──────────────────────────────────────
fig, ax = plt.subplots(figsize=(9, 7))
wedge_props = dict(width=0.6, edgecolor='white', linewidth=2)
wedges, texts, autotexts = ax.pie(values, labels=labels, colors=colors,
                                   autopct='%1.1f%%', startangle=90,
                                   wedgeprops=wedge_props, pctdistance=0.75)
for text in autotexts:
    text.set_fontsize(11)
    text.set_fontweight('bold')
ax.set_title('Figure 4.2: Triage Priority Proportional Distribution', fontsize=14, fontweight='bold', pad=15)
plt.tight_layout()
plt.savefig('radiology_pipeline/results/evaluation_plots/fig4_2_triage_pie.png', dpi=150, bbox_inches='tight')
plt.close()
print("Plot 2 saved")

# ── Plot 3: Top Specialist Referrals ───────────────────────────────────────
fig, ax = plt.subplots(figsize=(12, 6))
top10 = spec_counts.most_common(10)
s_labels = [s[0] for s in top10]
s_values = [s[1] for s in top10]
spec_colors = plt.cm.Set2(np.linspace(0, 1, len(s_labels)))
bars = ax.barh(s_labels[::-1], s_values[::-1], color=spec_colors[::-1], edgecolor='white', linewidth=1.2)
for bar, val in zip(bars, s_values[::-1]):
    ax.text(bar.get_width() + 0.2, bar.get_y() + bar.get_height()/2, str(val),
            va='center', fontweight='bold', fontsize=11)
ax.set_title('Figure 4.3: Top 10 Specialist Referrals Generated by the LLM', fontsize=14, fontweight='bold', pad=15)
ax.set_xlabel('Number of Referrals', fontsize=12)
ax.set_xlim(0, max(s_values) + 4)
plt.tight_layout()
plt.savefig('radiology_pipeline/results/evaluation_plots/fig4_3_specialist_referrals.png', dpi=150, bbox_inches='tight')
plt.close()
print("Plot 3 saved")

# ── Plot 4: Red Flag Detection ─────────────────────────────────────────────
fig, ax = plt.subplots(figsize=(8, 6))
rf_labels = ['Red Flags\nDetected', 'No Red Flags\nIdentified']
rf_values = [has_red_flag, no_red_flag]
rf_colors = ['#E24B4A', '#1D9E75']
bars = ax.bar(rf_labels, rf_values, color=rf_colors, edgecolor='white', linewidth=2, width=0.5)
for bar, val in zip(bars, rf_values):
    pct = val/len(rows)*100
    ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.3,
            f'{val}\n({pct:.1f}%)', ha='center', va='bottom', fontweight='bold', fontsize=13)
ax.set_title('Figure 4.4: Red Flag Detection Rate by LLM', fontsize=14, fontweight='bold', pad=15)
ax.set_ylabel('Number of Patients', fontsize=12)
ax.set_ylim(0, max(rf_values) + 10)
plt.tight_layout()
plt.savefig('radiology_pipeline/results/evaluation_plots/fig4_4_red_flag_detection.png', dpi=150, bbox_inches='tight')
plt.close()
print("Plot 4 saved")

# ── Plot 5: Model Processing Success Rate ──────────────────────────────────
fig, ax = plt.subplots(figsize=(8, 6))
proc_labels = ['Successfully\nProcessed', 'Failed\nProcessing']
proc_values = [parse_ok, parse_fail]
proc_colors = ['#1D9E75', '#E24B4A']
bars = ax.bar(proc_labels, proc_values, color=proc_colors, edgecolor='white', linewidth=2, width=0.5)
for bar, val in zip(bars, proc_values):
    pct = val/len(rows)*100
    ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.3,
            f'{val}\n({pct:.1f}%)', ha='center', va='bottom', fontweight='bold', fontsize=13)
ax.set_title('Figure 4.5: LLM Model Processing Success Rate', fontsize=14, fontweight='bold', pad=15)
ax.set_ylabel('Number of Patients', fontsize=12)
ax.set_ylim(0, max(proc_values) + 10)
plt.tight_layout()
plt.savefig('radiology_pipeline/results/evaluation_plots/fig4_5_processing_success.png', dpi=150, bbox_inches='tight')
plt.close()
print("Plot 5 saved")

# ── Plot 6: Critical vs Non-Critical Cases ─────────────────────────────────
fig, ax = plt.subplots(figsize=(9, 7))
critical = priority_counts.get('1',0) + priority_counts.get('2',0)
non_critical = len(rows) - critical
cn_labels = ['Critical/Emergency\n(Priority 1-2)', 'Urgent/Semi-urgent\n(Priority 3-4)', 'Non-urgent\n(Priority 5)']
cn_values = [critical, priority_counts.get('3',0)+priority_counts.get('4',0), priority_counts.get('5',0)]
cn_colors = ['#E24B4A', '#378ADD', '#1D9E75']
wedges, texts, autotexts = ax.pie(cn_values, labels=cn_labels, colors=cn_colors,
                                   autopct='%1.1f%%', startangle=90,
                                   wedgeprops=dict(edgecolor='white', linewidth=2),
                                   pctdistance=0.75)
for text in autotexts:
    text.set_fontsize(12)
    text.set_fontweight('bold')
ax.set_title('Figure 4.6: Clinical Urgency Classification of Patient Cohort', fontsize=14, fontweight='bold', pad=15)
plt.tight_layout()
plt.savefig('radiology_pipeline/results/evaluation_plots/fig4_6_urgency_classification.png', dpi=150, bbox_inches='tight')
plt.close()
print("Plot 6 saved")

# ── Plot 7: Cumulative Processing Progress ─────────────────────────────────
fig, ax = plt.subplots(figsize=(10, 6))
x = list(range(1, len(rows)+1))
y = list(range(1, len(rows)+1))
ax.plot(x, y, color='#185FA5', linewidth=2.5, label='Cumulative Patients Processed')
ax.fill_between(x, y, alpha=0.15, color='#185FA5')
ax.axhline(y=100, color='#E24B4A', linestyle='--', linewidth=1.5, label='Target (100 patients)')
ax.set_title('Figure 4.7: Cumulative Patient Processing by LLM Framework', fontsize=14, fontweight='bold', pad=15)
ax.set_xlabel('Patient Sequence Number', fontsize=12)
ax.set_ylabel('Cumulative Patients Processed', fontsize=12)
ax.legend(fontsize=11)
ax.set_xlim(1, 100)
ax.set_ylim(0, 110)
plt.tight_layout()
plt.savefig('radiology_pipeline/results/evaluation_plots/fig4_7_cumulative_processing.png', dpi=150, bbox_inches='tight')
plt.close()
print("Plot 7 saved")

